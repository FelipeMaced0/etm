    private void CadasVei_BT_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadasVei_BT_cadastrarActionPerformed
        String status;
        String tipoDeCombustivel;
        String tipoDeVeiculo;
        String tipoDeRota;
        String tipoDeCobranca;
        String placa;
        int capacidade;
        float autonomia;

        Veiculo nVeiculo=null;
        status = CadasVei_CB_status.getSelectedItem().toString();
        tipoDeCombustivel = CadasVei_CB_tipoCombustivel.getSelectedItem().toString();
        tipoDeRota = CadasVei_CB_tipoDeRota.getSelectedItem().toString();
        tipoDeVeiculo = CadasVei_CB_tipoDeVeiculo.getSelectedItem().toString();
        tipoDeCobranca = CadasVei_CB_tipoDeCobranca.getSelectedItem().toString();
        placa = CadasVei_CT_placa.getText();
        capacidade = Integer.parseInt(CadasVei_CT_capacidade.getText());
        autonomia = Float.parseFloat(CadasVei_CT_autonomia.getText());

        switch(tipoDeVeiculo){
            case "VLT":
                
                break;
            case "BRT":
                break;
            case "VAN":
                break;
            case "METRÔ":
                break;
            case "ÔNIBUS":
                nVeiculo = new Onibus(placa,"",status,tipoDeCombustivel,tipoDeRota,tipoDeCobranca,capacidade,autonomia);
                break;
            case "MICRO-ÔNIBUS":
        }
        if(nVeiculo != null){
            etm.cadastrarVeiculo(nVeiculo);
            CadasVei_P_id.setVisible(true);
            CadasVei_CT_id.setText(nVeiculo.getId());
        }
        
    }//GEN-LAST:event_CadasVei_BT_cadastrarActionPerformed

