    private void CadasVei_P_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadasVei_P_buscarActionPerformed
        Veiculo veiculo;
        String id;
        
        id = CadasVei_CT_id.getText();
        veiculo = etm.buscarVeiculo(id);
        if(veiculo != null){
            CadasVei_CB_status.setSelectedItem(veiculo.getStatus());
            CadasVei_CB_tipoCombustivel.setSelectedItem(veiculo.getCombustivel());
            CadasVei_CB_tipoDeRota.setSelectedItem(veiculo.getTipoDeRota());
            CadasVei_CB_tipoDeVeiculo.setSelectedItem(veiculo.getTipoDeVeiculo());
            CadasVei_CB_tipoDeCobranca.setSelectedItem(veiculo.getTipoDeCobranca());
            if(veiculo instanceof Onibus){
                CadasVei_CT_placa.setText(((Onibus)veiculo).getPlaca());
            }
            CadasVei_CT_capacidade.setText(String.valueOf(veiculo.getCapacidade()));
            CadasVei_CT_autonomia.setText(String.valueOf(veiculo.getAutonomia()));
        }
        else{
            exibirMsg("VEÍCULO NÃO ENCONTRADO");
        }
        
    }//GEN-LAST:event_CadasVei_P_buscarActionPerformed

